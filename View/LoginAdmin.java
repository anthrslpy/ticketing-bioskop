    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String code = fieldCode.getText();
        char[] passwordChars = fieldPassword.getPassword();
        String password = new String(passwordChars);
        
        if (code.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Code cannot be blank!");
        }
        else if(password.isEmpty()){
            JOptionPane.showMessageDialog(null, "Password cannot be blank!");
        }
         else if(authenticateUser(code, password)){
            String welcome = "Welcome back, " + user.getNama() + "!";
            JOptionPane.showMessageDialog(null, welcome, "Login Successful", JOptionPane.INFORMATION_MESSAGE);                   
            this.dispose();
            ViewAdmin viewAdmin = new ViewAdmin(user);
            viewAdmin.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "Code or password is incorrect","Error",JOptionPane.ERROR_MESSAGE);
        }
        fieldPassword.setText("");
    }//GEN-LAST:event_btnLoginActionPerformed

