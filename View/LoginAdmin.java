    private boolean authenticateUser(String code, String password) {
        for (Pengguna p : dataPengguna) {
            if (p instanceof Admin a){
                if (a.getCode().equals(code) && a.getPassword().equals(password)) {
                    user = p;
                    return true;
                }
            }
        }
        return false;
    }

