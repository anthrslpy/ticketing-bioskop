    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtTitle.getText().isEmpty() || txtGenre.getText().isEmpty() || txtDuration.getText().isEmpty() || jTextArea1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field film harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Film film = new Film(txtIdFilm.getText(), txtTitle.getText(), txtGenre.getText(), txtDuration.getText(), jTextArea1.getText(), txtPosterPath1.getText(), txtPosterPath2.getText());
        this.dispose();
        AdminAddJadwal adminAddJadwal = null;
        try {
            adminAddJadwal = new AdminAddJadwal(film, currentAdmin);
        } catch (IOException ex) {
            Logger.getLogger(AdminAddFilm.class.getName()).log(Level.SEVERE, null, ex);
        }
        adminAddJadwal.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

