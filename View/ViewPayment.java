    private void calculateTotal() {
        int pricePerSeat = currentStudio.equals("Reguler") ? 35000 : 50000;
        int total = selectedSeats.size() * pricePerSeat;
        lblJudul.setText(selectedFilm.getJudul());
        lblGenre.setText(selectedFilm.getGenre());
        lblDurasi.setText(selectedFilm.getDurasi());
        lblSelectedSeats.setText(String.join(", ", selectedSeats));
        lblTotalPrice.setText("Rp " + String.format("%,d", total));
    }
    
    private void simpanStatusKursi(String idJadwal, String nomorKursi, boolean terisi) {
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(KURSI_FILE, true))) {
            bw.write(idJadwal + "|" + nomorKursi + "|" + (terisi ? "terisi" : "kosong"));
            bw.newLine();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Failed to save seats data!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
