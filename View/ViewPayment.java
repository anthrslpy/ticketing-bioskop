    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        String message;
        message = "Payment Successful!" + "\n" +
                "Film: " + selectedFilm.getJudul() + "\n" +
                "Studio: " + currentStudio + "\n" +
                "Seats: " + String.join(", ", selectedSeats) + "\n" +
                "Total: " + lblTotalPrice.getText();

        JOptionPane.showMessageDialog(this, message, "Payment Confirmation",JOptionPane.INFORMATION_MESSAGE);
        
        for (String kursi : selectedSeats) {
            simpanStatusKursi(idJadwal, kursi, true);
        }
        
        this.dispose();
        ViewHome viewHome = new ViewHome(user);        
        viewHome.setVisible(true);
    }//GEN-LAST:event_btnConfirmActionPerformed

